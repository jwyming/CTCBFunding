PROCEDURE INSERT_INTO_T_HOLIDAY 
AS 
V_DATE NUMBER;
S_T_HOLIDAY_ID NUMBER;
BEGIN
DELETE FROM T_HOLIDAY;
SELECT SEQ_HOLIDAY.NEXTVAL INTO S_T_HOLIDAY_ID FROM DUAL;
SELECT TO_NUMBER(TO_CHAR(TO_DATE('20070101','YYYYMMDD'),'J')) INTO V_DATE FROM DUAL;
WHILE V_DATE <= TO_NUMBER(TO_CHAR(TO_DATE('20101231','YYYYMMDD'),'J')) LOOP
IF TO_NUMBER(TO_CHAR(TO_DATE(V_DATE,'J'),'D'))=7 OR TO_NUMBER(TO_CHAR(TO_DATE(V_DATE,'J'),'D'))=1 THEN 
INSERT INTO T_HOLIDAY VALUES(S_T_HOLIDAY_ID, TO_DATE(V_DATE,'J'), 1);
ELSE
INSERT INTO T_HOLIDAY VALUES(S_T_HOLIDAY_ID, TO_DATE(V_DATE,'J'), 0);
END IF;
V_DATE:=V_DATE+1;
SELECT SEQ_HOLIDAY.NEXTVAL INTO S_T_HOLIDAY_ID FROM DUAL;
END LOOP;
END;


